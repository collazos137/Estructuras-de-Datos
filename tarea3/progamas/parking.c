int main(){
	parking();
	return 0;
}

void parking(){
	int cases, i, numPark, j, ax;
	int sum, max, min, prom, ans;
	scanf("%d", &cases);
	for(i = 0; i < cases; ++i){
		scanf("%d", &numPark);
		sum = 0, max = -1, min = 100;
		for(j = 0; j < numPark; ++j){
			scanf("%d", &ax);
			sum += ax;
			if(ax > max){
				max = ax;
			}
			if(ax < min){
				min = ax;
			}
		}
		prom = sum/numPark;
		ans = ((prom - min )*2) + ((max - prom)*2);
		printf("%d\n", ans);
	}
}
